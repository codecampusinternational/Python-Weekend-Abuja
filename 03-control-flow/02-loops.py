# 02-loops.py
# This script demonstrates how to use loops in Python.

# Example 1: for loop
fruits = ["apple", "banana", "cherry"]
for fruit in fruits:
    print(fruit)  # Output: apple, banana, cherry (each on a new line)

# Example 2: while loop
count = 0
while count < 3:
    print("Count:", count)  # Output: Count: 0, Count: 1, Count: 2
    count += 1

