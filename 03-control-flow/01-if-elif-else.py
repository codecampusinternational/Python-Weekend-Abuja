# 01-if-elif-else.py
# This script demonstrates how to use if, elif, and else statements in Python.

# Example 1: Simple if statement
age = 18
if age >= 18:
    print("You are an adult.")  # Output: You are an adult.

# Example 2: if-elif-else
score = 85
if score >= 90:
    print("Grade: A")
elif score >= 80:
    print("Grade: B")  # Output: Grade: B
else:
    print("Grade: C")

