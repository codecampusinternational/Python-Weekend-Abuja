# 02-variables.py
# This script demonstrates how to use variables in Python.

# Example 1: Storing a number
age = 25
print("Age:", age)  # Output: Age: 25

# Example 2: Storing a string
name = "Alice"
print("Name:", name)  # Output: Name: Alice

# Example 3: Updating a variable
age = 26
print("Updated Age:", age)  # Output: Updated Age: 26

