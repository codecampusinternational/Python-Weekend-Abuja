# This script demonstrates how to use the Python prompt and perform basic operations.

# Example 1: Simple Math
print("2 + 3 =", 2 + 3)  # Output: 2 + 3 = 5
print("10 - 4 =", 10 - 4)  # Output: 10 - 4 = 6

# Example 2: Exponentiation
print("2 ** 3 =", 2 ** 3)  # Output: 2 ** 3 = 8 (2 raised to the power of 3)

# Example 3: Strings
print("Hello, Python!")  # Output: Hello, Python!

