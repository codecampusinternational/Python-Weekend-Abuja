# 03-strings.py
# This script demonstrates how to work with strings in Python.

# Example 1: Creating a string
greeting = "Hello, World!"
print(greeting)  # Output: Hello, World!

# Example 2: String concatenation
name = "Alice"
message = "Hello, " + name
print(message)  # Output: Hello, Alice

# Example 3: String methods
print(name.upper())  # Output: ALICE (converts to uppercase)
print(len(name))  # Output: 5 (length of the string)

